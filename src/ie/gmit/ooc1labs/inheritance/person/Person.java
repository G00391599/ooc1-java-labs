package ie.gmit.ooc1labs.inheritance.person;

public class Person {
	
	// Instance Variable
	private int age;
	
	public Person(int age) {
		this.age = age;
	}

	// Getter Method
	public int getAge() {
		return age;
	}
	
	// Setter Method
	public void setAge(int age) {
		this.age = age;
	}
	
}
